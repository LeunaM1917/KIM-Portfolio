function formatProjectName(name) {
    const labels = {
        'machine problem': 'Machine Problem',
        'DNSC-IDEAS': 'DNSC IDEAS',
        'Group': 'Group Project'
    };
    return labels[name] || name;
}

function isMobileProject(name) {
    return name === 'RentiFi';
}

function renderProjectCards() {
    const container = document.getElementById('projects-container');
    if (!container) return;

    Object.entries(projectsData).forEach(([year, yearProjects]) => {
        const section = document.createElement('div');
        section.className = 'mb-24 last:mb-0';

        const header = document.createElement('div');
        header.className = 'reveal mb-12 flex flex-col gap-4 border-b border-line pb-8 md:flex-row md:items-end md:justify-between';
        header.innerHTML = `
            <div>
                <p class="text-[11px] font-medium uppercase tracking-[0.35em] text-muted">Academic Work</p>
                <h3 class="mt-3 font-serif text-4xl font-medium text-ink md:text-5xl">${year}</h3>
            </div>
            <p class="text-sm text-muted">${Object.keys(yearProjects).length} projects</p>
        `;
        section.appendChild(header);

        const grid = document.createElement('div');
        grid.className = 'grid gap-px border border-line bg-line sm:grid-cols-2 lg:grid-cols-3';

        Object.entries(yearProjects).forEach(([name, images]) => {
            const title = formatProjectName(name);
            const card = document.createElement('button');
            card.type = 'button';
            card.className = 'project-card reveal-stagger group cursor-pointer bg-surface p-6 text-left transition-colors hover:bg-surface-alt md:p-8';
            card.dataset.title = title;
            card.dataset.year = year;
            card.dataset.images = JSON.stringify(images);
            if (isMobileProject(name)) card.dataset.mobile = 'true';

            card.innerHTML = `
                <div class="overflow-hidden border border-line">
                    <img src="${images[0]}" alt="${title}" class="aspect-[4/3] w-full object-cover object-top transition-transform duration-500 group-hover:scale-105">
                </div>
                <p class="mt-6 text-[11px] uppercase tracking-[0.25em] text-muted">${year}</p>
                <h4 class="mt-2 font-serif text-xl text-ink md:text-2xl">${title}</h4>
                <p class="mt-3 text-sm text-muted">${images.length} screenshot${images.length === 1 ? '' : 's'}</p>
                <span class="mt-4 inline-flex items-center gap-2 text-[11px] font-medium uppercase tracking-[0.3em] text-ink transition-colors group-hover:text-accent">View Project <span>↗</span></span>
            `;
            grid.appendChild(card);
        });

        section.appendChild(grid);
        container.appendChild(section);
    });
}

document.addEventListener('DOMContentLoaded', renderProjectCards);
